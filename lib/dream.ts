/** 검색어와 키워드/유의어를 양방향 포함 검색한다 */
export function searchDreams(query: string): DreamEntry[] {
  const q = query.trim();
  if (!q) return [];

  return DREAM_ENTRIES.filter((entry) => {
    const candidates = [entry.keyword, ...entry.aliases];
    return candidates.some((word) => q.includes(word) || word.includes(q));
  });
}
